        </li>
        <li>
          <strong>Overlay Events</strong>: Events related to the network
          overlay, such as nodes sending messages to each other.
        </li>
        <li>
          <strong>User Events</strong>: Events triggered by user actions, such
          as modifying trust configuration or network connections.
        </li>
      </ul>
