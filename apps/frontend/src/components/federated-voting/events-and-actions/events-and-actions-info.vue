          <strong>Protocol Actions</strong>: Actions that the voting protocol
          will take next, such as broadcasting a vote or gossipping to
          neighbors.
        </li>
        <li>
          <strong>User Actions</strong>: Actions that were initiated by the user
          and will be executed next.
        </li>
      </ul>
      <p>
        <strong>Disrupting Actions</strong>: You can disrupt protocol actions to
        test how the protocol behaves under failure conditions. Disrupted
        actions are highlighted in red. A node that has a disrupted action will
